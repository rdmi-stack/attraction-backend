const seed = async (): Promise<void> => {
  try {
    console.log('🌱 Starting database seed...\n');

    await connectDatabase();

    // Clear existing data
    console.log('🗑️  Clearing existing data...');
    await Promise.all([
      User.deleteMany({}),
      Tenant.deleteMany({}),
      Attraction.deleteMany({}),
      Category.deleteMany({}),
      Destination.deleteMany({}),
      Review.deleteMany({}),
      Booking.deleteMany({}),
    ]);

    // Seed categories
    console.log('📁 Seeding categories...');
    await Category.insertMany(categories);
    console.log(`   ✅ ${categories.length} categories created`);

    // Seed destinations
    console.log('🌍 Seeding destinations...');
    await Destination.insertMany(destinations);
    console.log(`   ✅ ${destinations.length} destinations created`);

    // Seed tenants
    console.log('🏢 Seeding tenants...');
    const createdTenants = await Tenant.insertMany(tenants);
    console.log(`   ✅ ${tenants.length} tenants created`);

    // Seed attractions with tenant associations
    console.log('🎢 Seeding attractions...');
    const attractionsWithTenants = attractions.map((a) => {
      const tenantIds = createdTenants.filter((t) => {
        const slug = t.slug;
        // 1. Direct slug match: attraction-based tenant matches its own attraction
        if (slug === a.slug) return true;
        // 2. Link by destination city (destination + attraction tenants)
        const city = a.destination.city.toLowerCase().replace(/\s+/g, '-');
        if (slug.includes(city)) return true;
        // Special case: Sharm El Sheikh → 'sharm'
        if (a.destination.city === 'Sharm El Sheikh' && slug.includes('sharm')) return true;
        // 3. Also link to national umbrella tenants
        if (slug === 'ctoureg') return true;
        if (slug === 'egypt-tour-booking') return true;
        return false;
      }).map((t) => t._id);

      return {
        ...a,
        tenantIds,
      };
    });

    await Attraction.insertMany(attractionsWithTenants);
    console.log(`   ✅ ${attractions.length} attractions created`);

    // Seed users (password will be hashed by pre-save hook)
    console.log('👥 Seeding users...');
    for (const userData of users) {
      await User.create({
        ...userData,
        assignedTenants: userData.role === 'super-admin' ? createdTenants.map((t) => t._id) : [],
      });
    }
    console.log(`   ✅ ${users.length} users created`);

    console.log('\n✅ Database seeded successfully!\n');
    console.log('📝 Test accounts:');
    console.log('   Admin: admin@attractions-network.com / Admin@123456');
    console.log('   Customer: customer@example.com / Customer@123');
    console.log('\n📊 Egypt Network Summary:');
    console.log(`   ✅ ${tenants.filter(t => t.status === 'active').length} Active Tenants`);
    console.log(`   ✅ ${tenants.filter(t => t.status === 'coming_soon').length} Coming Soon Tenants`);
    console.log(`   ✅ ${tenants.filter(t => t.status === 'pending').length} Pending Tenants`);
    console.log(`   ✅ ${tenants.length} Total Tenants`);
    console.log('   ✅ 13 Egypt Destinations');
    console.log('   ✅ 22 Egypt Attractions');

    await disconnectDatabase();
    process.exit(0);
  } catch (error) {
    console.error('❌ Seed failed:', error);
    process.exit(1);
  }
};

seed();
